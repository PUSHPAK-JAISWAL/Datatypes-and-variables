// this a program to show relational operator
// here true has value one 1
// here false has value zero 0
#include <stdio.h>
void main()
{
    int a = 10;
    int b = 20;
    printf("%d\n",a>b);// greater than 
    printf("%d\n",a<b);// less than
    printf("%d\n",a<=b);// less than equal to 
    printf("%d\n",a>=b);// greater than equal to
    printf("%d\n",a==b);// equal to equal to
    printf("%d\n",a!=b);// not equal to
}